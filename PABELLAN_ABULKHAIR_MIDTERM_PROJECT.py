# Variables
x = 5
y = 10

# User Input
user_input = int(input("Enter a number: "))

# Operators
result = x + y - user_input

# If-else selections
if result > 0:
    print("The result is positive.")
elif result < 0:
    print("The result is negative.")
else:
    print("The result is zero.")

# Functional and error-free
print("This code has been executed without any errors.")
